def main():
    var1 = "foo"
    var2 = "bar"
    print(var1 + var2)  # string concatenation "foobar"
    print(var1 * 2)  # string repetition "foofoo"
    print(var1 * 2 + var2 * 2)  # "foofoobarbar"

main()
